solve1=function(M){
  r=dim(M)[1]
  c=dim(M)[2]
  Q=diag(M)
  M1=M
  M1[,c]=rep(1,r)
  b=c(rep(0,c-1),1)
  p=solve(t(M1),b)
  M=-M/sum(p*Q)
  return(list(p,M))
}
A1=matrix(c(-2,1,1,1,-2,1,1,1,-2),3,byrow=T)
y1=solve1(A1)
